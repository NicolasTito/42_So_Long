#include "so_long.h"

void	exit_program(t_info *s_i)
{
	int	i;

	i = -1;
	while (++i < s_i->map->h)
		free(s_i->map->map[i]);
	free(s_i->map->map);
	free(s_i->map);
	exit(1);
}
